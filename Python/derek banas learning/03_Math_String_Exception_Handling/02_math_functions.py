import math

#ceil, floor and absolute value
print("ceil(4.4) = ", math.ceil(4.4))
print("floor(4.4) = ", math.floor(4.4))
print("fabs(-4.4)= ", math.fabs(-4.4))

#Factorial
print("factorial(4) =", math.factorial(4))

#return remainder of the division
print("fmod(5,4) = ", math.fmod(5,4))

#receive a float and return an integer
print("trunc(4.233) = ", math.trunc(4.233))

#return X^Y
print("pow(2,3) = " , math.pow(2,3))

# return square root
print("sqrt(4) = ", math.sqrt(4))

# Special values
print("math.e = ", math.e)
print("math.pi = ", math.pi)

# Return e^x
print("e^x = ", math.exp(4))

# Return the natural logarithm e * e * e ~= 20 so log(20) tells that e^3 ~= 20
print("log(20)= ", math.log(20))

# define the base
print("log(1000,10) = " , math.log(1000,10))

# base 10
print("log10(1000) = ", math.log10(1000))

#convert degree to radians and vice-versa
print("degree(1.5708) = ", math.degrees(1.5708))
print("radian(90) = ", math.radians(90))













